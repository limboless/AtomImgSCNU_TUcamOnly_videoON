print('---------比较运算符的输出类型为bool型-------------')
b,c=10,20
print('b大于c吗？',b>c)
print('b小于c吗？',b<c)
print('b等于c吗？',b==c) #value是否相等 
print('b<=c吗？',b<=c)
print('b不等于c吗？',b!=c)
'''一个变量由三部分组成，标识，类型和值
一个=是赋值运算符，两个==是比较运算符
== 比较的是对象的值， is 比较的是对象的标识'''
